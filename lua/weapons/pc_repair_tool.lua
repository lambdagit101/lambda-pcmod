local RepairTime = 10

function SWEP:PrimaryAttack()
    if SERVER then
        if self:GetOwner():GetEyeTrace().HitPos:DistToSqr(self:GetOwner():GetShootPos()) > 10000 then return end
        local entities = ents.FindAlongRay(self:GetOwner():GetShootPos(), self:GetOwner():GetEyeTrace().HitPos)
        for k, v in pairs(entities) do
            if v:GetClass() == "pc_case" or v:GetClass() == "pc_monitor" or v:GetClass() == "pc_prebuilt_linux" or v:GetClass() == "pc_prebuilt_windows" then
                    foundit = true
                    if v.DeviceHealth == v.MaxDHealth then
                        self:GetOwner():ChatPrint("The " .. v.PrintName .. " does not need any maintenance.")
                        return
                    end
                    self:GetOwner():ChatPrint("The " .. v.PrintName .. " is being fixed. Please wait 10 seconds...")
                    self:EmitSound("weapons/stunstick/spark" .. math.random(1, 3) .. ".wav")
                    local WaitTime = CurTime() + RepairTime
                    hook.Add("Think", "pc_repairprogress", function()
                        if self:GetOwner():GetEyeTrace().HitPos:DistToSqr(self:GetOwner():GetShootPos()) > 10000 or not v:IsValid() then
                            self:GetOwner():ChatPrint("Repairing failed!")
                            self:EmitSound("weapons/stunstick/spark" .. math.random(1, 3) .. ".wav")
                            hook.Remove("Think", "pc_repairprogress")
                            return
                        end
                        if CurTime() > WaitTime then
                            self:EmitSound("weapons/stunstick/spark" .. math.random(1, 3) .. ".wav")
                            v.DeviceHealth = v.MaxDHealth
                            self:GetOwner():ChatPrint("The " .. v.PrintName .. " has been fixed.")
                            hook.Remove("Think", "pc_repairprogress")
                            return
                        end
                            
                    end)
            end
        end
        if foundit == false then
            self:GetOwner():ChatPrint("No monitor or computer detected.")
        end
    end
end

